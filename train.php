    	O Train é um jogo que se encaixa na disciplina de Português e seu objetivo é desenvolver a habilidade de digitação da criança e seu conhecimento das palavras da língua portuguesa. <br />
O jogo funciona da seguinte forma:<br /><br />
•	O jogo sorteia 3 palavras  que aparecem escritas em um carrinho e a criança deve digitar as letras correspondentes das palavras de acordo com que elas aparecem. O jogo dura 1 minuto e a cada palavra digitada corretamente a criança ganha um determinado número de pontos.<br /><center><img src="images/prints/car1_facil2.png" width="200px"/></center><br />
•	No nível Fácil, aparecem apenas letras avulsas para a criança ir se acostumando com o teclado. Cada letra acertada soma 50 pontos.<br /><br />
•	No nível Médico aparecem palavras de 3 a 4 letras, acrescentando mais dificuldade e cobrando mais habilidade. Se for um carro de cor Verde, a palavra contida nele vai somar 100 pontos caso acertada e, depois, vai criar um novo carro no mesmo lugar, só que, Azul, portanto a palavra nele vai somar apenas 50 pontos. <br /><center><img src="images/prints/car1_medio.png" width="200px"/></center><br />
•	No nível Difícil são palavras maiores, de 4 a 6 letras e é acrescentando um carro da cor Roxa que, ao ser acertado, soma 200 pontos e, gera um Verde em seu lugar que vale 100 pontos e em seguida um Azul de 50 pontos.<br /><br />
•	Se o carro com a palavra percorrer toda a tela sem a criança acertar a palavra, ela perde o número de pontos de acordo com a cor do carro.<br /><center><img src="images/prints/car2_medio.png" width="200px"/></center><br />


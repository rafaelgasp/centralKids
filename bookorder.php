    	O Book Order é um jogo encaixado na disciplina de Matemática e seu principal objetivo é fazer com que as crianças aprendam a ordem dos principais números da Matemática e conheçam o valor de cada número. <Br />
O jogo funciona da seguinte forma:<br /><br />
•	O jogo sorteia a ordem dos números e, dependendo do nível, a quantidade dos números. A criança deve ordenar os números de forma ou crescente, ou decrescente, dependendo do nível antes do final de um cronômetro para acrescentar uma dificuldade ao jogo.<br /><center><img src="images/prints/1.png" width="30px"/><img src="images/prints/2.png" width="30px"/><img src="images/prints/3.png" width="30px"/><img src="images/prints/4.png" width="30px"/><img src="images/prints/5.png" width="30px"/></center><br />
•	Os números são dispostos em livros e, de acordo com o número, o tamanho de cada livros, para facilitar a associação do número com a vida real.<br /><br />
•	No nível Fácil, são números de 1 a 5, no Médio, de 1 a 10 e o Difícil de 1 a 14. Existem dois Modos de Jogo, o Crescente e o Decrescente. <br /><br />

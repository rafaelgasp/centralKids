O Memo Cards é um jogo da disciplina Lingua Inglesa, que possui como principal objetivo, fazer com que o usuário aprenda palavras comuns na lingua, além de aprimorar seu vocabulário e sua memória, já que a mesma é um dos enfoques da mesma.<Br /><Br />
• O jogo é basicamente de memória, porém com alguns atrativos a mais, que são recursos sonoros, cronometro e pontuação. O mesmo conterá três dificuldades (facil, medio e dificil), sendo que para cara dificuldade, terá cinco telas.<Br /><center><img src="images/prints/principal.png" width="77px"/><img src="images/prints/icone 2.png"  width="70px"/></center><Br />
• O usuário terá de clicar nas cartas sobrecristas no formulário do jogo e associar uma carta ou o som com a próxima a ser clicada. O primeiro nível possui 12 cartas, e a diferença pro nivel médio e dificil é o número das mesmas, sendo que quanto maior o nivel, mais cartas o jogo conterá. <Br /><Br />


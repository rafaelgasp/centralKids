    O jogo tem como finalidade desenvolver uma compreensão da criança em relação a símbolos matemáticos como os símbolos de adição e subtração, fazendo com que ela consiga montar operações matemáticos e ter um melhor raciocínio logico. O jogo será controlo do pelo mouse. Será dividido em três níveis: Fácil, Médio, Difícil.<Br /><center><img src="images/prints/starMath.png" width="200"/></center><br />
